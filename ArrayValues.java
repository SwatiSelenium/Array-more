package ArrayOnwards;

public class ArrayValues 
{
	public static void main(String[] args) 
	{
		
		String[] transport = new String[5];
		transport[0]="Train";
		transport[1]="Bus";
		transport[2]="Aeroplane";
		transport[3]="Car";
		transport[4]="Bike";
		
	
		System.out.println(transport[0]);
		System.out.println(transport[1]);
		System.out.println(transport[2]);
		System.out.println(transport[3]);
		System.out.println(transport[4]);
	}
}
